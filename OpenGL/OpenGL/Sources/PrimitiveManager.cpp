void PrimitiveManager::DrawCube(Shader* shader, Camera* camera, glm::vec3 position, glm::vec3 rotation, glm::vec3 scale, Texture * diffuse, Texture * specular)
{
	const Settings* settings = GlobalInstance::GetInstance()->GetSettings();

	SetupCube();

	shader->Use();

	// Pass data to shader
	{
		glm::mat4 modelMatrix = glm::mat4(1.0f);

		modelMatrix = glm::translate(modelMatrix, position);

		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.x), glm::vec3(1, 0, 0));
		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.y), glm::vec3(0, 1, 0));
		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.z), glm::vec3(0, 0, 1));

		modelMatrix = glm::scale(modelMatrix, scale);

		glm::mat4 projection;
		projection = glm::perspective(glm::radians(45.0f), settings->ViewportWidth / settings->ViewportHeight, 0.1f, 100.0f);
		glm::mat4 PVM = projection * camera->GetViewMatrix() * modelMatrix;

		shader->SetMat4("PVM", PVM);
		shader->SetMat4("uModel", modelMatrix);
		shader->SetMat4("viewPos", camera->GetViewMatrix());
	}

	shader->SetVec4("mColor", glm::vec4(1.0, 0.0, 0.0, 1.0));

	if (diffuse && specular)
	{
		//
		glActiveTexture(GL_TEXTURE0);
		diffuse->Bind();
		shader->SetInt("material.texture_diffuse1", 0);

		glActiveTexture(GL_TEXTURE1);
		specular->Bind();
		shader->SetInt("material.texture_specular1", 1);
		shader->SetFloat("material.shininess", 16.0);
		//
		glActiveTexture(GL_TEXTURE0);
	}

	// render Cube
	glBindVertexArray(cubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

void PrimitiveManager::DrawQuad(Shader & shader, Camera & camera, glm::vec3 position, glm::vec3 rotation, glm::vec3 scale,
	Texture * diffuse, 
	Texture * specular, 
	Texture * normal, 
	Texture * depth,
	unsigned int shadow)
{
	const Settings* settings = GlobalInstance::GetInstance()->GetSettings();

	SetupQuad();

	shader.Use();

	// Pass data to shader
	{
		glm::mat4 modelMatrix = glm::mat4(1.0f);

		modelMatrix = glm::translate(modelMatrix, position);

		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.x), glm::vec3(1, 0, 0));
		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.y), glm::vec3(0, 1, 0));
		modelMatrix = glm::rotate(modelMatrix, glm::radians(rotation.z), glm::vec3(0, 0, 1));

		modelMatrix = glm::scale(modelMatrix, scale);

		glm::mat4 projection;
		projection = glm::perspective(glm::radians(45.0f), settings->ViewportWidth / settings->ViewportHeight, 0.1f, 100.0f);
		glm::mat4 PVM = projection * camera.GetViewMatrix() * modelMatrix;

		shader.SetMat4("PVM", PVM);
		shader.SetMat4("uModel", modelMatrix);
		shader.SetVec3("viewPos", camera.GetPosition());
		shader.SetMat4("projection", projection);

		// shadow
		{
			float near_plane = 0.2f, far_plane = 40.5f;
			glm::mat4 lightProjection = glm::ortho(-10.0f, 10.0f, -10.0f, 10.0f, near_plane, far_plane);
			glm::mat4 lightView = glm::lookAt(glm::vec3(4.0f, 4.0f, 14.0f),
				glm::vec3(0.0f, 0.0f, 0.0f),
				glm::vec3(0.0f, 1.0f, 0.0f));
			glm::mat4 lightSpaceMatrix = lightProjection * lightView;

			shader.SetMat4("lightSpaceMatrix", lightSpaceMatrix);
		}
		
	}

	{
		shader.SetFloat("material.shininess", 16.0);
		shader.SetFloat("material.height_scale", 0.1f);

		//
		glActiveTexture(GL_TEXTURE0);
		diffuse->Bind();
		shader.SetInt("material.texture_diffuse1", 0);

		glActiveTexture(GL_TEXTURE1);
		specular->Bind();
		shader.SetInt("material.texture_specular1", 1);

		glActiveTexture(GL_TEXTURE2);
		normal->Bind();
		shader.SetInt("material.texture_normal1", 2);

		glActiveTexture(GL_TEXTURE3);
		depth->Bind();
		shader.SetInt("material.texture_depth1", 3);

		glActiveTexture(GL_TEXTURE4);
		glBindTexture(GL_TEXTURE_2D, shadow);
		shader.SetInt("shadowMap", 4);

		//
		glActiveTexture(GL_TEXTURE0);
	}

	glBindVertexArray(quadVAO);
	glDrawArrays(GL_TRIANGLES, 0, 6);
	glBindVertexArray(0);
}

void PrimitiveManager::DrawSkybox(Shader & shader,Camera & camera, Cubemap& cubemap)
{
	SetupSkybox();

	const Settings* settings = GlobalInstance::GetInstance()->GetSettings();

	glm::mat4 projection;
	projection = glm::perspective(glm::radians(45.0f), settings->ViewportWidth / settings->ViewportHeight, 0.1f, 100.0f);

	// draw skybox as last
	glDepthFunc(GL_LEQUAL);  // change depth function so depth test passes when values are equal to depth buffer's content
	shader.Use();
	glm::mat4 view = glm::mat4(glm::mat3(camera.GetViewMatrix())); // remove translation from the view matrix
	shader.SetMat4("view", view);
	shader.SetMat4("projection", projection);
	// skybox cube
	glBindVertexArray(skyboxVAO);
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_CUBE_MAP, cubemap.GetID());
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	glDepthFunc(GL_LESS); // set depth function back to default
}

